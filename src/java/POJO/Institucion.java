package POJO;


public class Institucion{
    
    private Integer idInstitucion;    
    private String nombreInstitucion;    
    private String tipoInstitucion;    
    private String pais;    
    private String paginaWeb;    
    private String email;    
    private Integer institucionActiva;

    /**
     * @return the idInstitucion
     */
    public Integer getIdInstitucion() {
        return idInstitucion;
    }

    /**
     * @param idInstitucion the idInstitucion to set
     */
    public void setIdInstitucion(Integer idInstitucion) {
        this.idInstitucion = idInstitucion;
    }

    /**
     * @return the nombreInstitucion
     */
    public String getNombreInstitucion() {
        return nombreInstitucion;
    }

    /**
     * @param nombreInstitucion the nombreInstitucion to set
     */
    public void setNombreInstitucion(String nombreInstitucion) {
        this.nombreInstitucion = nombreInstitucion;
    }

    /**
     * @return the tipoInstitucion
     */
    public String getTipoInstitucion() {
        return tipoInstitucion;
    }

    /**
     * @param tipoInstitucion the tipoInstitucion to set
     */
    public void setTipoInstitucion(String tipoInstitucion) {
        this.tipoInstitucion = tipoInstitucion;
    }

    /**
     * @return the pais
     */
    public String getPais() {
        return pais;
    }

    /**
     * @param pais the pais to set
     */
    public void setPais(String pais) {
        this.pais = pais;
    }

    /**
     * @return the paginaWeb
     */
    public String getPaginaWeb() {
        return paginaWeb;
    }

    /**
     * @param paginaWeb the paginaWeb to set
     */
    public void setPaginaWeb(String paginaWeb) {
        this.paginaWeb = paginaWeb;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the institucionActiva
     */
    public Integer getInstitucionActiva() {
        return institucionActiva;
    }

    /**
     * @param institucionActiva the institucionActiva to set
     */
    public void setInstitucionActiva(Integer institucionActiva) {
        this.institucionActiva = institucionActiva;
    }
    

    
}
