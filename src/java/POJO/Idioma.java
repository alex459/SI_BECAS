package POJO;

public class Idioma{

    
    private Integer idIdioma; 
    private Integer idDetalleUsuario;
    private String idioma;    
    private String nivelHabla;
    private String nivelLectura;
    private String nivelEscrito;

    /**
     * @return the idIdioma
     */
    public Integer getIdIdioma() {
        return idIdioma;
    }

    /**
     * @param idIdioma the idIdioma to set
     */
    public void setIdIdioma(Integer idIdioma) {
        this.idIdioma = idIdioma;
    }

    /**
     * @return the idDetalleUsuario
     */
    public Integer getIdDetalleUsuario() {
        return idDetalleUsuario;
    }

    /**
     * @param idDetalleUsuario the idDetalleUsuario to set
     */
    public void setIdDetalleUsuario(Integer idDetalleUsuario) {
        this.idDetalleUsuario = idDetalleUsuario;
    }

    /**
     * @return the idioma
     */
    public String getIdioma() {
        return idioma;
    }

    /**
     * @param idioma the idioma to set
     */
    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }

    /**
     * @return the nivelHabla
     */
    public String getNivelHabla() {
        return nivelHabla;
    }

    /**
     * @param nivelHabla the nivelHabla to set
     */
    public void setNivelHabla(String nivelHabla) {
        this.nivelHabla = nivelHabla;
    }

    /**
     * @return the nivelLectura
     */
    public String getNivelLectura() {
        return nivelLectura;
    }

    /**
     * @param nivelLectura the nivelLectura to set
     */
    public void setNivelLectura(String nivelLectura) {
        this.nivelLectura = nivelLectura;
    }

    /**
     * @return the nivelEscrito
     */
    public String getNivelEscrito() {
        return nivelEscrito;
    }

    /**
     * @param nivelEscrito the nivelEscrito to set
     */
    public void setNivelEscrito(String nivelEscrito) {
        this.nivelEscrito = nivelEscrito;
    }

    
    
    
}
