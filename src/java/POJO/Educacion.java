package POJO;


public class Educacion{

    
    private Integer idEducacion;
    private Integer idDetalleUsuario;    
    private String tipoEducacion;    
    private String gradoAlcanzado;    
    private String nombreInstitucion;    
    private Integer anio;    

    /**
     * @return the idEducacion
     */
    public Integer getIdEducacion() {
        return idEducacion;
    }

    /**
     * @param idEducacion the idEducacion to set
     */
    public void setIdEducacion(Integer idEducacion) {
        this.idEducacion = idEducacion;
    }

    /**
     * @return the idDetalleUsuario
     */
    public Integer getIdDetalleUsuario() {
        return idDetalleUsuario;
    }

    /**
     * @param idDetalleUsuario the idDetalleUsuario to set
     */
    public void setIdDetalleUsuario(Integer idDetalleUsuario) {
        this.idDetalleUsuario = idDetalleUsuario;
    }

    /**
     * @return the tipoEducacion
     */
    public String getTipoEducacion() {
        return tipoEducacion;
    }

    /**
     * @param tipoEducacion the tipoEducacion to set
     */
    public void setTipoEducacion(String tipoEducacion) {
        this.tipoEducacion = tipoEducacion;
    }

    /**
     * @return the gradoAlcanzado
     */
    public String getGradoAlcanzado() {
        return gradoAlcanzado;
    }

    /**
     * @param gradoAlcanzado the gradoAlcanzado to set
     */
    public void setGradoAlcanzado(String gradoAlcanzado) {
        this.gradoAlcanzado = gradoAlcanzado;
    }

    /**
     * @return the nombreInstitucion
     */
    public String getNombreInstitucion() {
        return nombreInstitucion;
    }

    /**
     * @param nombreInstitucion the nombreInstitucion to set
     */
    public void setNombreInstitucion(String nombreInstitucion) {
        this.nombreInstitucion = nombreInstitucion;
    }

    /**
     * @return the anio
     */
    public Integer getAnio() {
        return anio;
    }

    /**
     * @param anio the anio to set
     */
    public void setAnio(Integer anio) {
        this.anio = anio;
    }
    
    
}
