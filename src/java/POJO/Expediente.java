package POJO;


public class Expediente{

    
    private Integer idExpediente;
    private Integer idProgreso;
    private String estadoExpediente;

    /**
     * @return the idExpediente
     */
    public Integer getIdExpediente() {
        return idExpediente;
    }

    /**
     * @param idExpediente the idExpediente to set
     */
    public void setIdExpediente(Integer idExpediente) {
        this.idExpediente = idExpediente;
    }

    /**
     * @return the idProgreso
     */
    public Integer getIdProgreso() {
        return idProgreso;
    }

    /**
     * @param idProgreso the idProgreso to set
     */
    public void setIdProgreso(Integer idProgreso) {
        this.idProgreso = idProgreso;
    }

    /**
     * @return the estadoExpediente
     */
    public String getEstadoExpediente() {
        return estadoExpediente;
    }

    /**
     * @param estadoExpediente the estadoExpediente to set
     */
    public void setEstadoExpediente(String estadoExpediente) {
        this.estadoExpediente = estadoExpediente;
    }

    
    
    
}
