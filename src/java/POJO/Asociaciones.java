package POJO;


public class Asociaciones {

    
    private Integer idAsociacion;
    private Integer idDetalleUsuario;
    private String nombreAsociacion;

    /**
     * @return the idAsociacion
     */
    public Integer getIdAsociacion() {
        return idAsociacion;
    }

    /**
     * @param idAsociacion the idAsociacion to set
     */
    public void setIdAsociacion(Integer idAsociacion) {
        this.idAsociacion = idAsociacion;
    }

    /**
     * @return the idDetalleUsuario
     */
    public Integer getIdDetalleUsuario() {
        return idDetalleUsuario;
    }

    /**
     * @param idDetalleUsuario the idDetalleUsuario to set
     */
    public void setIdDetalleUsuario(Integer idDetalleUsuario) {
        this.idDetalleUsuario = idDetalleUsuario;
    }

    /**
     * @return the nombreAsociacion
     */
    public String getNombreAsociacion() {
        return nombreAsociacion;
    }

    /**
     * @param nombreAsociacion the nombreAsociacion to set
     */
    public void setNombreAsociacion(String nombreAsociacion) {
        this.nombreAsociacion = nombreAsociacion;
    }

    

    
    
}
