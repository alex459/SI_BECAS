    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        response.setContentType("text/html;charset=UTF-8"); //lineas importantes para leer tildes y ñ
        request.setCharacterEncoding("UTF-8"); //lineas importantes para leer tildes y ñ

        //servlet encargado de generar reportes de bitacora
        try {

            Map parametersMap = new HashMap();
            parametersMap.put("parametro1", "hola mundo jasper");

            ConexionBD conexionBD = new ConexionBD();
            conexionBD.abrirConexion();
            byte[] bytes = JasperRunManager.runReportToPdf("C:\\Users\\jose\\Documents\\NetBeansProjects\\SI_BECAS\\web\\REPORTES\\test_reporte.jasper", parametersMap, conexionBD.conn);
            conexionBD.cerrarConexion();
            response.setContentType("application/pdf");
            response.setContentLength(bytes.length);
            ServletOutputStream outputstream = response.getOutputStream();
            outputstream.write(bytes, 0, bytes.length);
            outputstream.flush();
            outputstream.close();

        } catch (Exception ex) {
            ex.printStackTrace();
            System.out.println("Error: " + ex);
        }

    }


